class variaveis{
    public static void main(String[] args){
        System.out.println("Executando projeto 2 - Criando Variaveis");

        int idade;
        //A variavel foi inicicalizada

        idade = 32;
        //o valor 32 foi atribuido a variavel idade

        System.out.println(idade);
        // OUTPUT: 32

        idade = 10;
        // a variavel idade agora possui o valor 10
        System.out.println(idade);
        

        idade = 10 + 5;
        System.out.println(idade);
        
        idade = 10 + 5 * 2;
        System.out.println(idade);

        idade = (10 + 5) * 2;
        System.out.println("Sua idade Ã© " + idade + "!");
    }
}